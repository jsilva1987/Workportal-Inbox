extension UIColor {
    
    //#00ACA2
    open class var bizagiGreen: UIColor {
        return UIColor(red: 37/255, green: 155/255, blue: 147/255, alpha: 1.0)
    }
    
    //#415866
    open class var bizagiDarkGreen: UIColor {
        return UIColor(red: 65/255, green: 88/255, blue: 102/255, alpha: 1)
    }
    
    //#00A7A3
    open class var bizagiLightGreen: UIColor {
        return UIColor(red: 0, green: 167/255, blue: 173/255, alpha: 1)
    }
    
    //#D6803C
    open class var bizagiOrange: UIColor {
        return UIColor(red: 214/255, green: 128/255, blue: 60/255, alpha: 1)
    }
    
    //#B2BB1E
    open class var bizagiYellow: UIColor {
        return UIColor(red: 178/255, green: 187/255, blue: 30/255, alpha: 1)
    }
    
    //#87969F
    open class var bizagiGrey: UIColor {
        return UIColor(red: 132/255, green: 148/255, blue: 157/255, alpha: 1)
    }
    
}
